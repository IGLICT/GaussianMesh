import jittor as jt

def log10(x):
    return jt.log(x) / jt.log(10.0)
    

def mse(img1, img2):
    return (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)

def psnr(img1, img2):
    mse = (((img1 - img2)) ** 2).view(img1.shape[0], -1).mean(1, keepdim=True)
    return 20 * log10(1.0 / jt.sqrt(mse))
